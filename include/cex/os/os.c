str_s
os__path__splitext(str_s path, bool return_ext)
{
    if (path.len == 0) {
        return str$("");
    }

    usize last_char_idx = path.len;
    usize last_dot_idx = path.len;

    for (usize i = path.len; i-- > 0;) {
        if (path.buf[i] == '.') {
            if (last_dot_idx == path.len) {
                last_dot_idx = i;
            }
        } else if (path.buf[i] == '/' || path.buf[i] == '\\') {
            break;
        } else if (last_dot_idx != path.len) {
            last_char_idx = i;
            break;
        }
    }
    if (last_char_idx < last_dot_idx) {
        if (return_ext) {
            return str.sub(path, last_dot_idx, 0);
        } else {
            return str.sub(path, 0, last_dot_idx);
        }

    } else {
        if (return_ext) {
            return str$("");
        } else {
            return path;
        }
    }
}


